function setUserData(questionID, userAnswer, questionText, dimensionName, points, reportStatement, questionType, subDimensionName) {

	// Grab the current user data from localStorage
	var userData = JSON.parse(localStorage.getItem("setData"));

	// Loop through it and update as needed
	for (i = 0; i < userData.length; i++) {

		if (userData[i].id == questionID) {

			userData[i].answer = userAnswer;
			userData[i].question = questionText;
			userData[i].dimension = dimensionName;
			userData[i].points = points;
			userData[i].report = reportStatement;
			userData[i].qtype = questionType;
			userData[i].subdimension = subDimensionName;

			localStorage.setItem("setData", JSON.stringify(userData));

		}

	}

}

function setCompleteData(dimension, isComplete) {

	// Grab the current dimension complete data from localStorage
	var completeData = JSON.parse(localStorage.getItem("completeData"));

	// Loop through it and update as needed
	for (i = 0; i < completeData.length; i++) {

		if (completeData[i].dimension == dimension) {

			completeData[i].isComplete = isComplete;

			localStorage.setItem("completeData", JSON.stringify(completeData));

		}

	}

}

function beginUserData() {

	// Grab the current user data and dimension complete data from localStorage
	var userData = JSON.parse(localStorage.getItem("setData"));
	var completeData = JSON.parse(localStorage.getItem("completeData"));

	// If there is no user data saved
	if (userData == null) {

		// It's their first time here, so give them the default user data
		userData = defaultUserData;

		// And set that in the localStorage
		localStorage.setItem("setData", JSON.stringify(userData));

	}
	// If there IS user data saved, then restore those answers
	else { restoreMyAnswersYo(userData); }

	// If there's no dimension complete data saved
	if (completeData == null) {

		// It's their first time here, so give them the default dimension complete data
		completeData = defaultCompleteData;

		// And set that in the localStorage
		localStorage.setItem("completeData", JSON.stringify(completeData));

	}
	// If there IS user data saved, then restore those answers
	else { addMyProgress(completeData); }

}

function convertToXML(userData) {

	var xmlHeader = '<?xml version="1.0" encoding="UTF-8"?>\n<questions>\n';
	var xmlEnd = '</questions>';
	var xmlString = "";
	var xmlDownload;

	for (i = 0; i < userData.length; i++) {

		// Ignore all of the questions the user didn't answer
		if (userData[i].answer != null) {
			var id = userData[i].id;
			var dimension = userData[i].dimension;
			var title = userData[i].question;
			var answer = userData[i].answer;
			var points = userData[i].points;

			var fieldname = dimension+'_'+id;

			answer = answer.replace("&", "&amp;");


			xmlString = xmlString + '\t<question>\n\t\t<fieldname>'+fieldname.toUpperCase()+'</fieldname>\n\t\t<id>'+id+'</id>\n\t\t<dimension>'+dimension+'</dimension>\n\t\t<title>'+title+'</title>\n\t\t<answer>'+answer+'</answer>\n\t\t<points>'+points+'</points>\n\t</question>\n';

			xmlDownload = xmlHeader+xmlString+xmlEnd;
		}

	}

	return xmlDownload;

}

function restoreMyAnswersYo(userData) {

	// Loop through all of the userdata we've been given
	for (i = 0; i < userData.length; i++) {

		var currentQuestion = $(".question[data-id='"+userData[i].id+"']");
		var userAnswer = userData[i].answer;
		var answerSetType;

		if (userData[i].id == "B7") { answerSetType = userData[i].answer}

		if (userData[i].id == "2.5") { $(".question[data-id='2.5']").attr("data-points", userData[i].points); }

		// If the question has been answered
		if (userData[i].question != null) {

			// Make it visible
			currentQuestion.attr("data-visible", "true");

			var hasField = currentQuestion.has("input").length || currentQuestion.has("textarea").length || currentQuestion.has("select").length;
			var enterLink = currentQuestion.has(".enterlink").length || currentQuestion.has(".checkquestion").length;
			var hasCheckbox = currentQuestion.has(".checkbox").length;
			var hasAnswerSet = currentQuestion.has(".answerset").length;
			var multipleFields = $(".question[data-id='"+userData[i].id+"'] input").length;
			var multipleSelect = $(".question[data-id='"+userData[i].id+"'] select[data-type='multiple']").length

			if (multipleSelect == true) {

				var selectedBoxes = userAnswer.split('|');

				for (n=0; n < selectedBoxes.length; n++) {

					$(".question[data-id='"+userData[i].id+"'] select[data-type='multiple']").after("<div class='option'>"+selectedBoxes[n]+"</div>");
					$(".option").click(function(){
						$(this).remove();
					});

				}

			}
			else if (hasField == true && multipleFields > 1) {

				var multipleAnswers = userAnswer.split(' - ');

				$(".question[data-id='"+userData[i].id+"'] input").each(function(q){

					$(this).val(multipleAnswers[q]);

				});


			}
			else if (hasField == true && enterLink == true) {

				// Make the user's answer selected
				$(".question[data-id='"+userData[i].id+"'] .surveybtn[data-answer='"+userAnswer+"']").addClass("selected");

				// Check if a user's answer is selected
				var questionSelected = $(".question[data-id='"+userData[i].id+"']").has(".surveybtn.selected").length;

				// If their answer isn't selected then it's a textfield answer, so make that visible and fill it in!
				if (questionSelected == false) {
					$(".question[data-id='"+userData[i].id+"'] input, .question[data-id='"+userData[i].id+"'] textarea, .question[data-id='"+userData[i].id+"'] select").val(userAnswer).attr("data-visible", "true");
					$(".question[data-id='"+userData[i].id+"'] .surveybtn[data-answer='Follow the link:']").addClass("selected");
					$(".question[data-id='"+userData[i].id+"'] .surveybtn[data-answer='Other']").addClass("selected");

				}

			}
			// Otherwise, it might be a checkbox field
			else if (hasCheckbox == true) {

				var selectedBoxes = userAnswer.split('|');

				for (p=0; p < selectedBoxes.length; p++) {

					$(".question[data-id='"+userData[i].id+"'] .checkbox .copy[data-copy='"+selectedBoxes[p]+"']").parent().addClass("selected");

					// Check if a user's answer is selected
					var questionSelected = $(".question[data-id='"+userData[i].id+"']").has(".checkbox.selected .copy[data-copy='Other']").length;

					if (questionSelected == true) {
						$(".question[data-id='"+userData[i].id+"'] input").val(selectedBoxes[p]).attr("data-visible", "true");

					}

				}
			}
			// If this question has a field of any type then we need to restore the users answer to a field
			else if ( hasField == true ) {

				$(".question[data-id='"+userData[i].id+"'] input, .question[data-id='"+userData[i].id+"'] textarea, .question[data-id='"+userData[i].id+"'] select").val(userAnswer);

				//$(".surveybtn.next").css("display", "block");

				if ($(".question[data-id='"+userData[i].id+"']").has("select").length && $(".question[data-id='"+userData[i].id+"']").has(".linkfield").length) {

					if (userAnswer != $(".question[data-id='"+userData[i].id+"'] select").val()) {
						$(".question[data-id='"+userData[i].id+"'] select").val("Other");
						$(".question[data-id='"+userData[i].id+"'] select").val("Other (please specify)");
						$(".question[data-id='"+userData[i].id+"'] input.linkfield").attr("data-visible", "true");
						$(".question[data-id='"+userData[i].id+"'] input.linkfield").text(userAnswer);
					}


				}

			}
			// If it's neither of the above, then it's just a generic yes or no question
			else {

				$(".question[data-id='"+userData[i].id+"'] .surveybtn[data-answer='"+userAnswer+"']").addClass("selected");

			}

		}

	}

}


function addMyProgress(completeData) {
	var describeComplete = 0;
	// Loop through all of the dimension complete data
	for (q = 0; q < completeData.length; q++) {
    var dimension = completeData[q].dimension;
		// If ther dimension is identify then we need to edit the top menu
		if (dimension == "Identify") {
			$("#dqt-stages-menu ul li.first.leaf a").attr("data-complete", completeData[q].isComplete);
      continue;
		}
		// Otherwise the dimension is a normal dimension, so we edit the submenu
		$("#dqt-dimensions-menu ul li").each(function(){
      var isComplete = completeData[q].isComplete;
			if (isComplete && $("a", this).text().trim() == dimension) {
				$("a", this).next().attr("data-complete", isComplete);
        describeComplete++;
			}

		});
		// If the dimension is Describe then check if it's complete by either the counter or it's data
		if (dimension == "Describe" && (describeComplete == 7 || completeData[q].isComplete == "true")) {
			$("#dqt-stages-menu ul li a").eq(1).attr("data-complete", "true");
			setCompleteData("Describe", "true");
		}
	}

}
